function [g] = gvec(t,x,p) 

  g1 = p(1)/p(5)+x(4)/p(2)-x(1)/p(5)-(p(6)*x(1)*x(3)*(cos(2.0*pi*t)*p(7)+sin(2.0*pi*t)*p(8)+1.0))/p(1);
  g2 = (p(6)*x(1)*x(3)*(cos(2.0*pi*t)*p(7)+sin(2.0*pi*t)*p(8)+1.0))/p(1)-x(2)/p(5)-x(2)/p(4);
  g3 = x(2)/p(4)-x(3)/p(3)-x(3)/p(5);
  g4 = x(3)/p(3)-x(4)/p(2)-x(4)/p(5);

g(1) = g1;
g(2) = g2;
g(3) = g3;
g(4) = g4;

end