function [g] = gvec(x,p) 

  g1 = p(1)/p(5)+1.0/p(2)*x(4)-1.0/p(5)*x(1)-1.0/p(1)*p(6)*x(1)*x(3);
  g2 = 1.0/p(1)*p(6)*x(1)*x(3)-1.0/p(5)*x(2)-1.0/p(4)*x(2);
  g3 = 1.0/p(4)*x(2)-1.0/p(3)*x(3)-1.0/p(5)*x(3);
  g4 = 1.0/p(3)*x(3)-1.0/p(2)*x(4)-1.0/p(5)*x(4);

g(1) = g1;
g(2) = g2;
g(3) = g3;
g(4) = g4;

end